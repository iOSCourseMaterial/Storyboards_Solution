- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender {
    if ([segue.identifier isEqualToString:@"AddTask"]) {
        UINavigationController *navCon = (UINavigationController *) segue.destinationViewController;
        RHAddTaskViewController *addTaskController = [navCon.viewControllers objectAtIndex:0];
        addTaskController.listViewController = self;
    } else if ([segue.identifier isEqualToString:@"EditTask"]) {
        RHEditTaskViewController *editTaskController = (RHEditTaskViewController *) segue.destinationViewController;
        editTaskController.task = [self.tasks objectAtIndex:self.tableView.indexPathForSelectedRow.row];
        editTaskController.taskListController = self;
    }
}

